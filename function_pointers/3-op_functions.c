#include <stdio.h>
/**
 * add_op - Addition of the numbers
 *@a: input
 *@b: input
 * Return: the sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub -Subtraction of a and b
 *@a: input
 *@b: input
 * Return:  the difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplication of the numbers
 *@a: input
 *@b: input
 * Return: the product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div -  division of the numbers
 *@a: input
 *@b: input
 * Return:  the result of the division .
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of thr numbers
 *@a: input
 *@b: input
 * Return: the remainder of the division.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
